n1 = input ('Digite algo')
print ('É um numero', n1.isalnum()) # isalnum – Verifique se todos os caracteres no texto são alfanuméricos
print('É alfabético', n1.isalpha()) # isalpha – Verifique se todos os caracteres no texto são letras
print('É minúscula', n1.islower()) 
print('É número', n1.isnumeric()) 
print('Tem espaço?', n1.isspace()) 
print('É maiúscula', n1.isupper()) 
